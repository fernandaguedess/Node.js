export default class Calculadora {

    static soma (a, b){
            return a + b
    }

    static sub (a, b){
            return a - b
    }
    
    static mult (a, b){
            return a * b

    }
    static div (a, b){
        return a / b
    }

    static fat (a, b){
        return a ** b
    }

    static kmMs (a){
        return a / 3.6
    }
}
